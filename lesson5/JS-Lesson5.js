//Addition of different: string+boolean, string+number, number+boolean
let additionStringBoolean = "Hello" + true;
let additionStringNumber = "Hello" + 7;
let additionNumberBoolean = 11 + false;

console.log(additionStringBoolean);
console.log(additionStringNumber);
console.log(additionNumberBoolean);

//Multiplication of different types: string * boolean, string * number, number * boolean
let multiplicationStringBoolean = "Hello" * true;
let multiplicationStringNumber = "5" * 7;
let multiplicationNumberBoolean = 7 * true;

console.log(multiplicationStringBoolean);
console.log(multiplicationStringNumber);
console.log(multiplicationNumberBoolean);

//Division of different types: string/boolean, string/number, number/Boolean
let divisionStringBoolean = "Hello" / true;
let divisionStringNumber = "35" / 7;
let divisionNumberBoolean = 7 / true;

console.log(divisionStringBoolean);
console.log(divisionStringNumber);
console.log(divisionNumberBoolean);

//An explicit conversion: number, string, boolean
let numberToString = String(5);
let stringToNumber = Number("123");
let booleanToString = String(false);
let stringToBoolean = Boolean("true");

console.log(numberToString);
console.log(stringToNumber);
console.log(booleanToString);
console.log(stringToBoolean);


